let x = 1;

//Relational Operators
console.log(x > 0); // Greater than
console.log(x >= 0); // Greater than equal to

console.log(x < 0); // Less than
console.log(x <= 0); // Less than equal to

//Equallity Operator
console.log(x == 1); // Value Checking
console.log(x != 1); // Reverse of value checking
console.log(x === 1); // Value as well as type checking
console.log(x !== 1); // Reverse of Strictly checking
